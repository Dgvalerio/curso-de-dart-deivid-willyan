// 01 - Introdução a Variáveis
void main() {
  String nome = "Davi";
  print(nome);

  int idade = 21;
  print(idade);

  double altura = 1.75;
  print(altura);

  bool eHomem = true;
  print(eHomem);

  List<String> nomeCompleto = ["Davi", "Gonçalves", "Valério"];
  print(nomeCompleto);
  print(nomeCompleto[0]);
  print('${nomeCompleto[0]} ${nomeCompleto[1]} ${nomeCompleto[2]}');
}
